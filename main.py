def counter(string):
    d = {'Спец.символы': 0, 'Буквы': 0, 'Цифры': 0}
    for i in string:
        if i.isalpha():
            d['Буквы'] += 1
        if i.isalpha():
            d['Цифры'] += 1
    else:
        d['Спец.символы'] += 1
    print("Количество цифр: ", d['Цифры'])
    print("Количество букв: ", d['Буквы'])
    print("Количество спец.символов: ", d['Спец.символы'])
    return d['Буквы'], d['Цифры'], d['Спец.символы']
